        </div>
      </div>
    </el-col>
  </el-row>
</template>

<script>
import CountTo from 'vue-count-to'
import { getAgentReportApi } from '@/api/agent'


export default {
  components: {
    CountTo
  },
  data() {
    return {
      Balance: 0,
      withdraw_sum: 0, // 提现总额
      charge_sum: 0, // 充值总额
      user_count: 0,
      benefit:0
    }
  },
  methods: {
    getAgentReport() {
      var _this =this;
      getAgentReportApi().then((res) => {
        if (res.code === 20000) {
          _this.Balance = res.items.surplus_money?parseFloat(res.items.surplus_money):0;
          _this.withdraw_sum = res.items.total_withdraw_under?parseFloat(res.items.total_withdraw_under):0;
          _this.charge_sum = res.items.total_charge_under?parseFloat(res.items.total_charge_under):0;
          _this.user_count = res.items.users_count?parseFloat(res.items.users_count):0;
          _this.benefit = res.items.total_benefit?parseFloat(res.items.total_benefit):0;
        }
      })
    }
  },
  mounted(){
    this.getAgentReport()
  }
}
</script>

